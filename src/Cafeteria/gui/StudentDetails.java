    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  
    try {
    DefaultTableModel StudentDetailsTable = (DefaultTableModel) studentTable.getModel();
    StudentDetailsTable.setRowCount(0); // Clear existing rows
    
    try (FileReader fileReader = new FileReader("src/Cafeteria/data/Students.txt");
         BufferedReader bufferedReader = new BufferedReader(fileReader)) {
        String studentDetails;
        while ((studentDetails = bufferedReader.readLine()) != null) {
            String[] student_details = studentDetails.split(",");
            StudentDetailsTable.addRow(new Object[]{student_details[0].toUpperCase(), student_details[2]});
        }
        
    } catch (FileNotFoundException ex) {
        Logger.getLogger(StudentDetails.class.getName()).log(Level.SEVERE, null, ex);
    } catch (IOException ex) {
        Logger.getLogger(StudentDetails.class.getName()).log(Level.SEVERE, null, ex);
    }
    } catch (Exception e) {
    // Handle any other exceptions if necessary
            }
     

    }//GEN-LAST:event_jButton1ActionPerformed

