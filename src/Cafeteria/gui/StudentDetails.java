    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
    int[] selectedRows = studentTable.getSelectedRows();   
    
    if (selectedRows.length > 0) {
        Students m = new Students();       
        
        for (int i = selectedRows.length - 1; i >= 0; i--) {
            try {
                String username = (String) studentTable.getValueAt(selectedRows[i], 0);
                String Deleter = StaffMainPage.logged_staff;
                LoginLogger.logUserDeletion(Deleter, username);
                m.deleteUser(username);
                DefaultTableModel model = (DefaultTableModel) studentTable.getModel();
                model.removeRow(selectedRows[i]);
            } catch (IOException ex) {
                Logger.getLogger(StudentDetails.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    } else {
        JOptionPane.showMessageDialog(this,"No rows are Selected, Please Select One to Delete"); 
    }
    }//GEN-LAST:event_jButton3ActionPerformed

